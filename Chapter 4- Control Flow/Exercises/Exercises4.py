# Exercise 4: Stages of Life

Age = 20

if Age < 2:

    print("You are a infant.")

elif Age < 4:

    print("You are a toddler.")
    
elif Age < 13:

    print("You are a teenager.")

elif Age < 20:

    print("You are a young adult.")

elif Age < 65-70:

    print("You are an elder.")

else:

    print("You are a senior citizen.")